function a(key, vars){
  const str = ADMIN_I18N[ADMIN_LANG]?.[key] ?? ADMIN_I18N.de?.[key] ?? key;
  if(!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_,k) => vars[k] ?? '');
}

function setAdminLang(lang){
  localStorage.setItem('sxh-admin-lang', lang);
  location.reload();
}

// Render lang switcher HTML
function adminLangSwitcher(){
  return ['de','en','tr'].map(l =>
    `<button class="al-btn ${l===ADMIN_LANG?'active':''}" onclick="setAdminLang('${l}')">${l.toUpperCase()}</button>`
  ).join('');
}
